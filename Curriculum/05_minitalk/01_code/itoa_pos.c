#include "minitalk.h"

void	itoa_pos(int n)
{
	char	c;

	if (n < 1)
		return ;
	c = n % 10 + '0';
	if (9 < n)
	{
		n /= 10;
		itoa_pos(n);
	}
	write(1, &c, 1);
}
